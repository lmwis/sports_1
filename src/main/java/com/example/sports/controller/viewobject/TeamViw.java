package com.example.sports.controller.viewobject;

public class TeamViw {
	/*团队编号*/
	private int id;
	/*团队名称*/
	private String name;
	/*性别*/
	private String sex;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	
}
