package com.example.sports.bean;
/**
  * 学院表所对应实体类
 * @author Misaki
 *
 */
public class College {
	/*学院编号*/
	private Integer id;
	/*学院名称*/
	private String name;
	public int getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
