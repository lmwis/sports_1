package com.example.sports.bean;
/**
 * 记录各学院奖牌数类
 * @author Misaki
 *
 */
public class MedalNum {
	/*学院编号*/
	private int collegeId;
	/*奖牌数目*/
	private int num;
	public int getCollegeId() {
		return collegeId;
	}
	public void setCollegeId(int collegeId) {
		this.collegeId = collegeId;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	
}
